#ifndef _BSP_OPFLOW_UPIXELSLC306_CMD_H_
#define _BSP_OPFLOW_UPIXELSLC306_CMD_H_

#define OPFLOW_UPIXELSLC306_SENSOR_IIC_ADDR		(0xDC)
#define OPFLOW_UPIXELSLC306_RESOLUTION_CM		(0.005263157f)	/*1m高度下 1个像素对应的位移,单位cm*/
												 

/*OPFLOW_UPIXELSLC306 配置指令*/
typedef enum
{
	OPFLOW_UPIXELSLC306_STATR_CFG  = 0xAA,	/*开启配置指令*/
	OPFLOW_UPIXELSLC306_MODULE_CFG = 0xAB,	/*模块内部参数配置指令*/
	OPFLOW_UPIXELSLC306_SENSOR_CFG = 0xBB,	/*传感器参数配置指令*/
	OPFLOW_UPIXELSLC306_FINISH_CFG = 0xDD,	/*关闭配置指令*/
}OPFLOW_UPIXELSLC306_CFG_CMD;

/*OPFLOW_UPIXELSLC306 配置应答*/
typedef enum
{
	OPFLOW_UPIXELSLC306_ACK_SUCC     = 0x00,
	OPFLOW_UPIXELSLC306_ACK_IIC_FAIL = 0x01,
	OPFLOW_UPIXELSLC306_ACK_XOR_FAIL = 0x02,
}OPFLOW_UPIXELSLC306_CFG_ACK;

/*1.OPFLOW_UPIXELSLC306_STATR_CFG*/
/*应答: 无
  结果:	无*/

/*2.OPFLOW_UPIXELSLC306_MODULE_CFG*/
/*应答: 状态值( 0x00: 成功, 0x01: IIC 无应答, 0x02: XOR 校验错误)
  结果:	配置成功后模块返回: 0x00, 0xAB*/

/*3.OPFLOW_UPIXELSLC306_SENSOR_CFG*/
/*应答: 状态值( 0x00: 成功, 0x01: IIC 无应答, 0x02: XOR 校验错误)
  结果:	配置成功后模块返回: 0x00, 0xBB*/

/*4.OPFLOW_UPIXELSLC306_FINISH_CFG*/
/*应答: 无
  结果:	无*/

#endif
